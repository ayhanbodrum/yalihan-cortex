{{-- Animation Script --}}
<script>
    document.addEventListener('DOMContentLoaded', () => {
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    const countElement = entry.target;
                    const target = parseInt(countElement.getAttribute('data-target'));

                    let count = 0;
                    const duration = 2000; // 2 seconds
                    const increment = target / (duration / 16); // 60fps

                    const timer = setInterval(() => {
                        count += increment;
                        if (count >= target) {
                            clearInterval(timer);
                            count = target;
                        }
                        countElement.textContent = Math.floor(count) + '+';
                    }, 16);

                    observer.unobserve(countElement);
                }
            });
        }, {
            threshold: 0.5
        });

        document.querySelectorAll('.animate-count').forEach(counter => {
            observer.observe(counter);
        });
    });
</script>
